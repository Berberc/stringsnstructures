package modules.vectorAnalysis;

public class VectorAnalysisData {

	private double valueAverage;
	private double aberrationSum;
	private double pivotPercentage;
	
	/**
	 * @return the valueAverage
	 */
	public double getValueAverage() {
		return valueAverage;
	}
	/**
	 * @param valueAverage the valueAverage to set
	 */
	public void setValueAverage(double valueAverage) {
		this.valueAverage = valueAverage;
	}
	/**
	 * @return the aberrationSum
	 */
	public double getAberrationSum() {
		return aberrationSum;
	}
	/**
	 * @param aberrationSum the aberrationSum to set
	 */
	public void setAberrationSum(double aberrationSum) {
		this.aberrationSum = aberrationSum;
	}
	/**
	 * @return the pivotPercentage
	 */
	public double getPivotPercentage() {
		return pivotPercentage;
	}
	/**
	 * @param pivotPercentage the pivotPercentage to set
	 */
	public void setPivotPercentage(double pivotPercentage) {
		this.pivotPercentage = pivotPercentage;
	}
}
